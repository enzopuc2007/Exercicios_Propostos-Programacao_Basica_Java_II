//Programa 3 - Realização do exercicio 1 do módulo passado com For

public class Program3{

	public static void main(String[] args){
	
		int Cont,Soma;
		
		Soma=0;
		
		for(Cont=1;Cont<=10;Cont++){
			
			Soma = Soma + 1;
			
		}
		
		System.out.printf("A soma dos 10 primeiros numeros eh: %d",Soma);
	}
}